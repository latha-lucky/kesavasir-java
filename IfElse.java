class IfElse
{
	public static void check(int num)
	{
	  if (num>25)
	  { 
	  	System.out.println(num+"is greater then 25");
      }
	else
	{
      System.out.println(num+"is lesser then 25");
    }
}
    public static void main(String[] args)
     {
	   check(26);
	   check(4);
	 }
}
