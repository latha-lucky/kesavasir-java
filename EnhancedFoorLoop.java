package colletionprogramms.com;

public class EnhancedFoorLoop {
	public static void main(String[] args) {
		int []a= {10,20,30};
		for(int i:a) {
			System.out.println(i);
		}
		double[]marks= {23.5,45.3,56.7};
		for(double b:marks) {
			System.out.println(b);
	}
		String[]s= {"apple","mango","watermelon"};
		for(String f:s) {
			System.out.println(f);
		}

	
}
}



